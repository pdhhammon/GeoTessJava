	static final double Q1 = 0.5*PI;
	static final double Q2 = PI;
	static final double Q3 = 1.5*PI;
	static final double Q4 = 2.*PI;
	
	static final int N = 2048;
	
	private static final double dx = 1. / (Q1 / N);

	public static double sin(double x)
	{
		double sign = 1.;
		
		if (x < 0.)
		{
			sign = -1.;
			x = -x;
		}
		x = x % Q4;
		
		switch ((int)(x/Q1))
		{
		case 0: 
			x = x * dx;
			break;
		case 1: 
			x = (PI-x) * dx;
			break;
		case 2: 
			x = (x-PI) * dx;
			sign *= -1.;
			break;
		case 3: 
			x = (Q4-x) * dx;
			sign *= -1.;
			break;
		default:
			throw new java.lang.IllegalArgumentException();
		}
		
		int i = (int) x;
		if (i >= N) return sign;
		return sign * (sinTable[i] + (x - i)*(sinTable[i + 1] - sinTable[i]));
	}


	public static double cos(double x)
	{
		x = Math.abs(x) % Q4;
		
		switch ((int)(x/Q1))
		{
		case 0: 
			x = (Q1-x) * dx;
			int i = (int) x;
			if (i >= 2048) return 1.;
			return sinTable[i] + (x - i)
					* (sinTable[i + 1] - sinTable[i]);
		case 1: 
			x = (x - Q1) * dx;
			i = (int) x;
			if (i >= N) return -1.;
			return -sinTable[i] - (x - i)
					* (sinTable[i + 1] - sinTable[i]);
		case 2: 
			x = (Q3-x) * dx;
			i = (int) x;
			if (i >= N) return -1.;
			return -sinTable[i] - (x - i)
					* (sinTable[i + 1] - sinTable[i]);
		case 3: 
			x = (x-Q3) * dx;
			i = (int) x;
			if (i >= N) return 1.;
			return sinTable[i] + (x - i)
					* (sinTable[i + 1] - sinTable[i]);
		default:
			throw new java.lang.IllegalArgumentException();
		}
	}


	/**
	 * Convert geographic lat, lon into a geocentric unit vector. The
	 * x-component points toward lat,lon = 0, 0. The y-component points toward
	 * lat,lon = 0, PI/2. The z-component points toward north pole.
	 * 
	 * @param lat
	 *            geographic latitude in radians.
	 * @param lon
	 *            longitude in radians.
	 * @return 3 component unit vector.
	 */
	public static double[] getVector(double lat, double lon)
	{
		double[] vector = new double[3];
		VectorGeo.getVector(lat, lon, vector);
		return vector;
	}

	/**
	 * Convert geographic lat, lon into a geocentric unit vector. The
	 * x-component points toward lat,lon = 0, 0. The y-component points toward
	 * lat,lon = 0, PI/2 The z-component points toward north pole.
	 * 
	 * @param lat
	 *            geographic latitude in radians.
	 * @param lon
	 *            longitude in radians.
	 * @param vector
	 *            3 component unit vector.
	 *            
	 * @return length of vector prior to normalization.
	 */
	public static void getVector(double lat, double lon, double[] vector)
	{
		
		lat = getGeoCentricLatitude(lat);

		// z component of v is sin of geocentric latitude.
		vector[2] = sin(lat);

		// set lat = to cos of geocentric latitude
		lat = cos(lat);

		// compute x and y components of v
		vector[0] = lat * cos(lon);
		vector[1] = lat * sin(lon);
		
		normalize(vector);
	}

	/**
	 * Return geocentric latitude given a geographic latitude
	 * This is an approximation of 
	 * atan(0.9933056199770992 * tan(lat));
	 * 
	 * @param lat
	 *            geographic latitude in radians
	 * @return geocentric latitude in radians
	 */
	public static double getGeoCentricLatitude(double lat)
	{
		// convert lat from geographic to geocentric latitude.
		// the constant 0.9933 is (1-e*e) where e is the eccentricity of the
		// earth as defined by the GRS80 ellipsoid.

		//return atan(0.9933056199770992 * tan(lat));

		if (lat < 0)
		{
			lat = -lat / (Q1 / (geocentric.length - 1));
			int i = (int) lat;
			return -geocentric[i] - (lat - i)
					* (geocentric[i + 1] - geocentric[i]);
		}
		else
		{
			lat = lat / (Q1 / (geocentric.length - 1));
			int i = (int) lat;
			return geocentric[i] + (lat - i)
					* (geocentric[i + 1] - geocentric[i]);
		}
	}

	/**
	 * Return geographic latitude given a geocentric latitude.
	 * 
	 * This is an approximation of 
	 * atan(tan(lat) / 0.9933056199770992);
	 * 
	 * @param lat
	 *            geocentric latitude in radians
	 * @return geographic latitude in radians
	 */
	public static double getGeoGraphicLatitude(double lat)
	{
		// the constant 0.9933 is (1-e*e) where e is the eccentricity of the
		// earth as defined by the GRS80 ellipsoid.

		//return atan(tan(lat) / 0.9933056199770992);

		if (lat < 0)
		{
			lat = -lat / (Q1 / (geographic.length - 1));
			int i = (int) lat;
			return -geographic[i] - (lat - i)
					* (geographic[i + 1] - geographic[i]);
		}
		else
		{
			lat = lat / (Q1 / (geographic.length - 1));
			int i = (int) lat;
			return geographic[i] + (lat - i)
					* (geographic[i + 1] - geographic[i]);
		}
	}

	/**
	 * Normalizes the input vector to unit length. Returns the length of the
	 * vector prior to normalization.
	 * 
	 * @param u
	 *            vector<double>
	 * @return length of the vector prior to normalization ( >= 0.)
	 */
	public static double normalize(double[] u)
	{
		double len = u[0] * u[0] + u[1] * u[1] + u[2] * u[2];
		if (len > 0.)
		{
			len = sqrt(len);
			u[0] /= len;
			u[1] /= len;
			u[2] /= len;
		}
		else
		{
			len = u[0] = u[1] = u[2] = 0.0;
		}
		return len;
	}

